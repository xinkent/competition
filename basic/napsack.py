class SimpleDFS:
    """
    primitiveãªDFS
    """
    def __init__(self, N, W, wv):
        self.N = N
        self.W = W
        self.wv = wv

    def rec(self, n, uplimit_w):
        ans = 0
        if n == N:
            return 0

        # nç•ªç›®ã®å“ç‰©ã‚’ä½¿ãˆãªã„å ´åˆ
        if wv[n][0] > uplimit_w:
            return self.rec(n + 1, uplimit_w)

        # nç•ªç›®ã®å“ç‰©ã‚’ä½¿ãˆã‚‹å ´åˆ
        else:
            ans = max(
                self.rec(n + 1, uplimit_w),
                self.rec(n+1, uplimit_w - wv[n][0]) + wv[n][1]
            )
            return ans 

    def dfs_solve(self):
        print(self.rec(0, W))


